#include "libft.h"

int	is_space(char *str)
{
	int	i;

	if (!str)
		return (0);
	i = ft_strlen(str);
	while (*str == ' ')
	{
		i--;
		++str;
	}
	if (i == 0)
		return (1);
	return (0);
}
